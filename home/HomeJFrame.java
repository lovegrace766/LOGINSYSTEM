    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();

        if (row < 0) {
            javax.swing.JOptionPane.showMessageDialog(null, "Please select any record from table");
        } else {

            // GET VALUES FROM TABLE
            this.id = (int) jTable1.getValueAt(row, 0);
            String firstName = jTable1.getValueAt(row, 1).toString();
            String lastName = jTable1.getValueAt(row, 2).toString();
            String gender = jTable1.getValueAt(row, 3).toString();
            String province = jTable1.getValueAt(row, 4).toString();
            Object dateObj = jTable1.getValueAt(row, 5);
            String address = jTable1.getValueAt(row, 6).toString();

            // SET VALUES TO FIELDS
            jTextFieldFirstName.setText(firstName);
            jTextFieldLastName.setText(lastName);
            jComboBoxGender.setSelectedItem(gender);
            jComboBoxProvince.setSelectedItem(province);
            jTextAreaAddress.setText(address);

            // DATE (important)
            if (dateObj instanceof java.util.Date) {
                jDateChooserDate.setDate((java.util.Date) dateObj);
            }
        }
    


    }//GEN-LAST:event_jTable1MouseClicked


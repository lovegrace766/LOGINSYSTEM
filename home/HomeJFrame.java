    private void jButtonInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInsertActionPerformed

        // get values
        String first_Name = jTextFieldFirstName.getText();
        String last_Name = jTextFieldLastName.getText();
        String gender = (String) jComboBoxGender.getSelectedItem();
        String province = (String) jComboBoxProvince.getSelectedItem();
        Date date = jDateChooserDate.getDate();
        String address = jTextAreaAddress.getText();

        // simple check
        if (first_Name.isEmpty() || last_Name.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(null, "Please fill all fields");
            return;
        }

        // show values (TEST first)
        javax.swing.JOptionPane.showMessageDialog(null,
                "First Name: " + first_Name
                + "\nLast Name: " + last_Name
                + "\nGender: " + gender
                + "\nProvince: " + province
                + "\nDate: " + date
                + "\nAddress: " + address);

        //now make object of homeBean class...
        HomeBean homeBean = new HomeBean(0, first_Name, last_Name, gender, province, date, address); //passing value to constructor

        //homeBal object
        com.home.HomeBal homeObj = new com.home.HomeBal();
        homeObj.insert(homeBean);   // through object we are passing object to insert method
        loadTable();
    }//GEN-LAST:event_jButtonInsertActionPerformed
